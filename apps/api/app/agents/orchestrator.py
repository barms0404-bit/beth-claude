"""BETH — the Chief-of-Staff orchestrator.

Pipeline for one report:
  1. Build a market brief for the window.
  2. Dispatch the specialist roster concurrently (one bad specialist never kills
     the report — failures are logged and skipped).
  3. Route every chart request to the Chart Specialist.
  4. Aggregate specialist recommendations into a ranked Top 50, resolving
     duplicate coverage by conviction score.
  5. Run BETH's own synthesis turn to write the executive summary and call out
     conflicts between specialists.
  6. Return a Report; persistence + email happen downstream.
"""

from __future__ import annotations

import asyncio
import json
import logging
from datetime import datetime, timezone

from app.agents.base import build_context
from app.agents.chart_specialist import ChartSpecialist
from app.agents.registry import roster_for
from app.schemas import (
    ChartSpec,
    Recommendation,
    Report,
    ReportSlot,
    SpecialistOutput,
)
from app.services import market_data

logger = logging.getLogger("beth")

DISCLAIMER = (
    "This material is produced by Armstrong Arikat Private Wealth Group for "
    "informational purposes only. It is not investment advice, an offer, or a "
    "solicitation to buy or sell any security. Opinions are generated by an "
    "automated multi-agent research system and are subject to compliance review. "
    "Past performance does not guarantee future results."
)

_BETH_SYSTEM = """\
You are BETH, Chief of Staff and research orchestrator at Armstrong Arikat Private
Wealth Group. Your 15 specialists have each filed structured findings. Synthesize
them into a report for the firm's advisors.

Your job:
- Write a tight executive summary (2-4 paragraphs) of the research window.
- Explicitly surface DISAGREEMENTS between specialists and give your adjudication.
- Set the tone: measured, professional, no hype. This is research, not advice.

Respond with ONLY a JSON object:
{
  "title": "report title",
  "summary": "executive summary including conflict adjudication"
}
"""

_SLOT_TITLES = {
    ReportSlot.market_prep: "Market Prep — 7:30 AM",
    ReportSlot.mid_day: "Mid-Day Update — 11:00 AM",
    ReportSlot.market_close: "Market Close — 1:30 PM",
}


class Beth:
    """The orchestrator. One instance per process is fine; runs are stateless."""

    def __init__(self) -> None:
        self.chart_specialist = ChartSpecialist()

    async def generate_report(
        self, slot: ReportSlot, *, market_brief: str | None = None
    ) -> Report:
        brief = market_brief or self._default_brief(slot)
        roster = roster_for(slot)
        logger.info("BETH dispatching %d specialists for %s", len(roster), slot.value)

        # 2. Dispatch specialists concurrently; isolate failures.
        context = build_context(slot=slot, market_brief=brief)
        results = await asyncio.gather(
            *(spec.run(context) for spec in roster),
            return_exceptions=True,
        )
        outputs: list[SpecialistOutput] = []
        for spec, res in zip(roster, results):
            if isinstance(res, Exception):
                logger.warning("Specialist %s failed: %s", spec.key, res)
            else:
                outputs.append(res)

        # 3. Charts.
        charts = await self._render_charts(outputs)

        # 4. Aggregate the Top 50.
        recommendations = await self._rank_recommendations(outputs)

        # 5. BETH synthesis.
        title, summary = await self._synthesize(slot, outputs)

        return Report(
            slot=slot,
            title=title,
            summary=summary,
            recommendations=recommendations,
            charts=charts,
            disclaimer=DISCLAIMER,
            generated_at=datetime.now(timezone.utc),
        )

    # -- pipeline stages ----------------------------------------------------
    async def _render_charts(self, outputs: list[SpecialistOutput]) -> list[ChartSpec]:
        jobs = [
            self.chart_specialist.render(req, requested_by=out.agent_key)
            for out in outputs
            for req in out.chart_requests
        ]
        if not jobs:
            return []
        done = await asyncio.gather(*jobs, return_exceptions=True)
        return [c for c in done if isinstance(c, ChartSpec)]

    async def _rank_recommendations(
        self, outputs: list[SpecialistOutput]
    ) -> list[Recommendation]:
        """Dedup by symbol (keep the highest-conviction sponsor), rank, take Top 50."""
        best: dict[str, tuple[float, str, str]] = {}  # symbol -> (score, thesis, agent)
        for out in outputs:
            for rec in out.recommendations:
                sym = rec.symbol.upper()
                if sym not in best or rec.score > best[sym][0]:
                    best[sym] = (rec.score, rec.thesis, out.agent_key)

        ranked = sorted(best.items(), key=lambda kv: kv[1][0], reverse=True)[:50]
        quotes = await market_data.get_quotes([sym for sym, _ in ranked])

        recs: list[Recommendation] = []
        for i, (sym, (_score, thesis, agent)) in enumerate(ranked, start=1):
            q = quotes.get(sym)
            recs.append(
                Recommendation(
                    rank=i,
                    symbol=sym,
                    name=sym,  # TODO(step 2 wiring): resolve from `securities` table
                    price=q.price if q else None,
                    dailyPct=q.daily_pct if q else None,
                    ytdPct=q.ytd_pct if q else None,
                    thesis=thesis,
                    leadSpecialist=agent,
                )
            )
        return recs

    async def _synthesize(
        self, slot: ReportSlot, outputs: list[SpecialistOutput]
    ) -> tuple[str, str]:
        if not outputs:
            return _SLOT_TITLES[slot], "No specialist findings were produced for this window."

        digest = json.dumps(
            [
                {
                    "specialist": o.agent_key,
                    "headline": o.headline,
                    "stance": o.stance.value,
                    "top_picks": [r.symbol for r in o.recommendations[:5]],
                }
                for o in outputs
            ],
            indent=2,
        )
        from app.services.anthropic_client import call_agent

        reply = await call_agent(
            system_prompt=_BETH_SYSTEM,
            user_message=f"Report window: {slot.value}\n\nSpecialist findings:\n{digest}",
            temperature=0.4,
        )
        data = reply.as_json()
        return data.get("title", _SLOT_TITLES[slot]), data.get("summary", "")

    @staticmethod
    def _default_brief(slot: ReportSlot) -> str:
        """Placeholder brief until the market-data pipeline feeds BETH live context.

        TODO(step 3): replace with a real brief assembled from Polygon snapshots,
        the economic calendar, and the overnight earnings tape.
        """
        return (
            f"[{slot.value}] No live market brief wired yet. Specialists should reason "
            "from their domain mandate and flag where live data is required."
        )

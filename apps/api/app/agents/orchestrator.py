"""BETH — the Chief-of-Staff orchestrator.

Two entry points, one specialist-dispatch core:

  generate_report(slot)  — full report: dispatch -> charts -> Top 50 -> synthesis.
  refresh_top50()        — the 15-minute engine poll: dispatch -> ingest -> re-rank.

The Top 50 ranking is owned entirely by `app.engine.top50` — Beth feeds it and
reads it back, so there is one ranking algorithm, not two.
"""

from __future__ import annotations

import asyncio
import json
import logging
from datetime import datetime, timezone

from app.agents.base import build_context
from app.agents.chart_specialist import ChartSpecialist
from app.agents.registry import SPECIALISTS, roster_for
from app.agents.verifier import verify_high_conviction
from app.engine.top50 import engine
from app.schemas import (
    ChartSpec,
    Recommendation,
    RecommendationVerification,
    Report,
    ReportSlot,
    SpecialistReport,
    Top50Snapshot,
)
from app.services.macro_calendar import macro_event_today

# Specialist keys whose picks count as "growth/thematic" for Beth's
# orchestration rules (contrarian check, bull-skew detection).
GROWTH_THEMATIC_KEYS: set[str] = {
    "ai_datacenter",
    "training_chip",
    "inference_stack",
    "robotics",
    "quantum",
    "tech_generalist",
    "consumer_internet",
}

# Trigger threshold for the bull-skew rule.
HIGH_CONVICTION = 8       # Rule 1: any single growth pick >= this fires contrarian.
BULL_SKEW_FLOOR = 7       # Rule 4: count of growth picks >= this triggers when >= count.
BULL_SKEW_COUNT = 4

logger = logging.getLogger("beth")

DISCLAIMER = (
    "This material is produced by Armstrong Arikat Private Wealth Group for internal "
    "research purposes only. It is not investment advice, an offer, or a solicitation "
    "to buy or sell any security. Opinions are generated by an automated multi-agent "
    "research system and are subject to compliance review. Past performance does not "
    "guarantee future results."
)

_BETH_SYSTEM = """\
You are BETH, Chief of Staff and research orchestrator at Armstrong Arikat Private
Wealth Group. You report to Brian, the Portfolio Manager. Your 15 specialists have
each filed a structured report. Synthesize them for Brian.

Your job:
- Write a tight executive summary (2-4 paragraphs) of the research window.
- Explicitly surface DISAGREEMENTS between specialists and give your adjudication.
- Escalate any risk flags the specialists raised that Brian must see.
- Tone: measured, dry, professional, no hype. This is research, not advice.

Respond with ONLY a JSON object:
{
  "title": "report title",
  "summary": "executive summary, conflict adjudication, escalated risk flags"
}
"""

_SLOT_TITLES = {
    ReportSlot.market_prep: "Market Prep — 7:30 AM",
    ReportSlot.mid_day: "Mid-Day Update — 11:00 AM",
    ReportSlot.market_close: "Market Close — 1:30 PM",
}


class Beth:
    """The orchestrator. One instance per call is fine; ranking state lives in the engine."""

    def __init__(self) -> None:
        self.chart_specialist = ChartSpecialist()

    # -- specialist dispatch (shared core) ----------------------------------
    async def dispatch(
        self, slot: ReportSlot, *, market_brief: str | None = None
    ) -> list[SpecialistReport]:
        """Run the slot's specialist roster concurrently; isolate failures."""
        brief = market_brief or self._default_brief(slot)
        roster = roster_for(slot)
        logger.info("BETH dispatching %d specialists for %s", len(roster), slot.value)

        context = build_context(slot=slot, market_brief=brief)
        results = await asyncio.gather(
            *(spec.run(context) for spec in roster),
            return_exceptions=True,
        )
        outputs: list[SpecialistReport] = []
        for spec, res in zip(roster, results):
            if isinstance(res, Exception):
                logger.warning("Specialist %s failed: %s", spec.key, res)
            else:
                outputs.append(res)
        return outputs

    # -- full report --------------------------------------------------------
    async def generate_report(
        self, slot: ReportSlot, *, market_brief: str | None = None
    ) -> Report:
        outputs = await self.dispatch(slot, market_brief=market_brief)

        # Three LLM-heavy passes run concurrently:
        #   - render charts requested by specialists
        #   - PSV verifier on conviction>=8 ideas
        #   - Beth's "focused contrarian" pass (Value Investor again, if
        #     growth/thematic team produced high-conviction picks or skewed bullish)
        charts, verifications, bear_case = await asyncio.gather(
            self._render_charts(outputs),
            self._verify_high_conviction(outputs),
            self._maybe_focused_contrarian(outputs, slot=slot),
        )

        # Feed the Top 50 engine and read the ranking back — single source.
        engine.ingest(outputs)
        snapshot = await engine.rebuild()
        recommendations = self._recommendations_from(snapshot)

        # Macro event-day rule: on FOMC / CPI / NFP days, Fixed Income leads.
        macro_event = macro_event_today()
        lead_key = "fixed_income" if macro_event else None

        title, summary = await self._synthesize(slot, outputs)

        return Report(
            slot=slot,
            title=title,
            summary=summary,
            recommendations=recommendations,
            charts=charts,
            specialist_reports=outputs,
            verifications=verifications,
            lead_specialist_key=lead_key,
            bear_case_addendum=bear_case,
            macro_event=macro_event,
            disclaimer=DISCLAIMER,
            generated_at=datetime.now(timezone.utc),
        )

    # -- 15-minute Top 50 poll ---------------------------------------------
    async def refresh_top50(self) -> Top50Snapshot:
        """Poll specialists and re-rank the Top 50. Driven by the scheduler."""
        outputs = await self.dispatch(ReportSlot.mid_day)
        engine.ingest(outputs)
        return await engine.rebuild()

    # -- pipeline stages ----------------------------------------------------
    @staticmethod
    async def _verify_high_conviction(
        outputs: list[SpecialistReport],
    ) -> list[RecommendationVerification]:
        """PSV runs only on `new_ideas` with conviction>=8. One task per specialist."""
        jobs = [
            verify_high_conviction(
                sr.new_ideas, agent_key=sr.agent_key, persona=sr.specialist
            )
            for sr in outputs
        ]
        nested = await asyncio.gather(*jobs, return_exceptions=True)
        flat: list[RecommendationVerification] = []
        for result in nested:
            if isinstance(result, list):
                flat.extend(result)
            elif isinstance(result, Exception):
                logger.warning("Verifier batch failed: %s", result)
        return flat

    @staticmethod
    async def _maybe_focused_contrarian(
        outputs: list[SpecialistReport], *, slot: ReportSlot
    ) -> SpecialistReport | None:
        """Beth's contrarian-check rules:

        - Rule 1: any growth/thematic pick with conviction >= HIGH_CONVICTION (8)
          triggers a focused bear-case check on that name.
        - Rule 4: when growth/thematic specialists collectively file >= BULL_SKEW_COUNT
          picks at conviction >= BULL_SKEW_FLOOR, dispatch a dedicated bear case.

        Both trigger the same downstream action — one extra Value Investor pass
        with a focused contrarian context. Returns None when neither fires.
        """
        growth_picks: list[tuple[str, str, int, str]] = []
        bull_skew_picks: list[tuple[str, str, int]] = []
        for sr in outputs:
            if sr.agent_key not in GROWTH_THEMATIC_KEYS:
                continue
            for idea in sr.new_ideas:
                if idea.conviction_1_10 >= HIGH_CONVICTION:
                    growth_picks.append(
                        (sr.specialist, idea.ticker, idea.conviction_1_10, idea.thesis)
                    )
                if idea.conviction_1_10 >= BULL_SKEW_FLOOR:
                    bull_skew_picks.append((sr.specialist, idea.ticker, idea.conviction_1_10))

        bull_skew = len(bull_skew_picks) >= BULL_SKEW_COUNT
        if not growth_picks and not bull_skew:
            return None

        spec = SPECIALISTS.get("value_investor")
        if spec is None:
            logger.warning("value_investor specialist missing — skipping contrarian pass.")
            return None

        triggers: list[str] = []
        if growth_picks:
            triggers.append(f"{len(growth_picks)} growth/thematic pick(s) at conviction>=8")
        if bull_skew:
            triggers.append(
                f"team consensus skews bullish ({len(bull_skew_picks)} picks at "
                f"conviction>={BULL_SKEW_FLOOR})"
            )

        picks_block = "\n".join(
            f"- {persona}: {ticker} (conviction {conv}/10) — {thesis}"
            for persona, ticker, conv, thesis in growth_picks[:12]
        ) or "(no single name at conviction>=8 — the bull-skew rule fired)"

        bull_block = "\n".join(
            f"- {persona}: {ticker} ({conv}/10)"
            for persona, ticker, conv in bull_skew_picks[:20]
        )

        focus = (
            f"FOCUSED CONTRARIAN CHECK — triggered by: {'; '.join(triggers)}.\n\n"
            "The growth/thematic side of the team has filed conviction picks that "
            "require your bear-case rigor BEFORE this report reaches Brian. Do "
            "NOT repeat your general-pass output. For each high-conviction name "
            "below, produce the contrarian read: consensus thesis as you read "
            "it, your counter-view, the valuation risk, the downside scenario, "
            "and a verdict (defensible | overhyped | trap).\n\n"
            f"HIGH-CONVICTION GROWTH PICKS (rule 1):\n{picks_block}\n\n"
            f"FULL BULL-SKEW ROSTER (rule 4):\n{bull_block}\n\n"
            "Output channels — use these fields in your standard SpecialistReport:\n"
            "- key_takeaway: one sentence on the most stretched name and your "
            "  overall verdict on the bullish skew.\n"
            "- covered_names_commentary: one entry per checked ticker; narrative "
            "  is your contrarian view + verdict; action is 'avoid' | 'watch' | 'hold'.\n"
            "- new_ideas: leave empty unless you have a SHORT-side equivalent value "
            "  setup that pairs with one of these names.\n"
            "- risk_flags: any name you judge a trap."
        )

        context = build_context(
            slot=slot, market_brief=Beth._default_brief(slot), focus=focus
        )
        try:
            return await spec.run(context)
        except Exception as exc:
            logger.warning("Focused contrarian pass failed: %s", exc)
            return None

    async def _render_charts(self, outputs: list[SpecialistReport]) -> list[ChartSpec]:
        jobs = [
            self.chart_specialist.render(
                out.chart_request, requested_by=out.specialist, agent_key=out.agent_key
            )
            for out in outputs
            if out.chart_request is not None
        ]
        if not jobs:
            return []
        done = await asyncio.gather(*jobs, return_exceptions=True)
        return [c for c in done if isinstance(c, ChartSpec)]

    @staticmethod
    def _recommendations_from(snapshot: Top50Snapshot) -> list[Recommendation]:
        """Map engine Top 50 entries onto the API-facing Recommendation model."""
        return [
            Recommendation(
                rank=e.rank,
                symbol=e.ticker,
                name=e.company_name,
                price=e.price,
                dailyPct=e.day_change_pct,
                ytdPct=e.ytd_change_pct,
                conviction=max(1, min(10, round(e.conviction_avg))),
                thesis=e.thesis_summary,
                leadSpecialist=e.lead_specialist,
                lastUpdated=snapshot.snapshot_time,
            )
            for e in snapshot.entries
        ]

    async def _synthesize(
        self, slot: ReportSlot, outputs: list[SpecialistReport]
    ) -> tuple[str, str]:
        if not outputs:
            return _SLOT_TITLES[slot], "No specialist findings were produced for this window."

        digest = json.dumps(
            [
                {
                    "specialist": o.specialist,
                    "key_takeaway": o.key_takeaway,
                    "top_ideas": [
                        {"ticker": idea.ticker, "conviction": idea.conviction_1_10}
                        for idea in o.new_ideas[:5]
                    ],
                    "risk_flags": o.risk_flags,
                }
                for o in outputs
            ],
            indent=2,
        )
        from app.services.anthropic_client import call_agent

        reply = await call_agent(
            system_prompt=_BETH_SYSTEM,
            user_message=f"Report window: {slot.value}\n\nSpecialist filings:\n{digest}",
            temperature=0.4,
        )
        data = reply.as_json()
        return data.get("title", _SLOT_TITLES[slot]), data.get("summary", "")

    @staticmethod
    def _default_brief(slot: ReportSlot) -> str:
        """Placeholder brief until the market-data pipeline feeds Beth live context.

        TODO(step 3): replace with a real brief assembled from Polygon snapshots,
        the economic calendar, and the overnight earnings tape.
        """
        return (
            f"[{slot.value}] No live market brief wired yet. Specialists should reason "
            "from their coverage universe and flag where live data is required."
        )
